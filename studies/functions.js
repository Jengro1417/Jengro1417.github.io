/*
*0. FUNCTIONS
*   
*   Functions encapsulate a block of code and let us run that code block as many times as we want 
*
*1. Creating a Function:
*   -2 stages
*       -Declaration/Definition 
*       -Call/Invocation/Execution 
*2. Declaration:
*   -creating the action you want the computer to perform
*   -do not put any concrete values in declaration(only use placeholders)
*   -placeholders = Parameters
*  
* Simple Function Example:
*/
    function subtract(number1, number2){
        return number1 - number2;
    }
/*
*3. Building a Function:
*   -function keyword
*   -function name
*   -parameters inside ()
*   -function body inside {}
*       -contains action it will perform usually involves returning a value 
*
*4. Function Call:
*   -in order to use function we have to invoke data by using a function call 
*   -2 parts:
*       -function name
*       -arguements in()
*  Example:
*/
    function subtract (number1, number2){
        return number1 - number2;
    }
    subtract(14, 17);
